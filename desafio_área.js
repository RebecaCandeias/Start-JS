/* fórmula da área da circunferência = PI * raio * raio 
*  PI está em letra maiúscula porque é uma constante
*  PI = 3.14 */ 

const pi = 3.14
let raio = 10
let area = pi * raio * raio

console.log("área =" + " " + area + "m²")

console.log(Math.PI) // o próprio JS tem o MATH para calcular o PI mais preciso

areaDaCircunferencia = Math.PI * raio * raio

console.log("área:" + areaDaCircunferencia + "m²")

